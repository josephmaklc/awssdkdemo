package com.optimal.solution.awsutils;

public class DynamoAttribute {
	String type;
	Object value;

	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public Object getValue() {
		return value;
	}
	public void setValue(Object value) {
		this.value = value;
	}

}
